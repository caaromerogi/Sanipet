package com.sofka;

public class Owner {
    private String dni;
    private String name;
    private String surname;
    private String cellphone;
    private int age;

    public Owner(String dni, String name, String surname, String cellphone, int age) {
        this.dni = dni;
        this.name = name;
        this.surname = surname;
        this.cellphone = cellphone;
        this.age = age;
    }
}
